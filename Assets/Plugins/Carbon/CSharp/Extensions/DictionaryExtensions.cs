namespace Carbon
{
	/// <summary>
	/// Dictionary Extensions
	/// </summary>
	public static class DictionaryExtensions
	{
		/*
		* データ構造の拡張メソッドを軽く作るんではありません.
		*
		* 1. データ構造には其々の特性と設計コンセプトがあります.
		*    他のデータ構造の特性を利用したい場合, 該当データ構造を使ってください.
		*    強引な真似はバグと処理負荷の源になりますので, 厳守しましょう.
		*
		* 2. Dictionary のスキャン系と検索系拡張メソッドを IReadOnlyDictionary の拡張メソッド (IReadOnlyDictionaryExtensions) に追加してください.
		*
		* 3. Dictionary の操作系拡張メソッドを IDictionary の拡張メソッド (IDictionaryExtensions) に追加してください.
		*/
	}
}
