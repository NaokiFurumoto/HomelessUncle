using System;
using System.Collections;

namespace Carbon
{
	public sealed class WaitForTask : IEnumerator
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// WaitForTask
		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		private bool m_IsWaiting = false;

		public WaitForTask(Action<Action> task)
		{
			m_IsWaiting = true;
			task(() => m_IsWaiting = false);
		}

		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// IEnumerator Implementation
		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		object IEnumerator.Current {
			get {
				return null;
			}
		}

		bool IEnumerator.MoveNext()
		{
			return m_IsWaiting;
		}

		void IEnumerator.Reset()
		{

		}
	}
}